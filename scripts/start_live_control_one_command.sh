export MINE_TELEOP_SIGNALING_REMOTE_PORT="$SIGNALING_REMOTE_PORT"
export MINE_TELEOP_CONTROL_PLANE_SERVER="$CONTROL_PLANE_SERVER"
export MINE_TELEOP_CONTROL_PLANE_CONSOLE="$CONTROL_PLANE_CONSOLE"
export MINE_TELEOP_OPEN_BROWSER="$OPEN_BROWSER"

cd "$REPO_ROOT"
exec scripts/start_live_control_plane_tunnel.sh
