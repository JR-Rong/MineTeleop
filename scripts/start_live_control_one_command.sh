export MINE_TELEOP_DRIVER_CONSOLE_LOCAL_PORT="$DRIVER_CONSOLE_LOCAL_PORT"
export MINE_TELEOP_SIGNALING_LOCAL_PORT="$SIGNALING_LOCAL_PORT"
